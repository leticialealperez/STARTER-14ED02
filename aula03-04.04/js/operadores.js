let numero1 = 6
let numero2 = 2
let resultado


// OPERADORES

// =================================================================
// 1 - ARITMÉTICOS
// +, -, *, /, %, **, ++, --

// soma (+)
// valor1 + valor2
resultado = numero1 + numero2 // 6 + 2 = 8


// subtração (-)
// valor1 - valor2
resultado = numero1 - numero2 // 6 - 2 = 4


// multiplicação (*)
// valor1 * valor2
resultado = numero1 * numero2 //  6 * 2 = 12

// divisão (/)
// valor1 / valor2
resultado = numero1 / numero2 //  6 / 2 = 3

// resto ou módulo (%)
// valor1 % valor2
resultado = numero1 % numero2 // 6 % 2 = 0


// exponenciação (**) - valor1 elevado ao valor2
// valor1 ** valor2
resultado = numero1 ** numero2 // 6 ** 2 = 36


// incremento (++)
// valor1 incrementado em +1
numero1++ // 6 + 1 = 7


// decremento (--)
// valor1 decrementado em -1
numero1-- // 7 - 1 = 6



// =================================================================
// 2 - ATRIBUIÇÃO 

// atribuição (=)
// valorEsquerda vai receber o valor da direita
numero1 = 10 // reatribuição


// atribui a soma (+=)
// numero1 = numero1 + 5
numero1 += 5 // 15


// atribui a subtração (-=)
// numero1 = numero1 - 5
numero1 -= 5 // 10


// atribui a multiplicação (*=)
// numero1 = numero1 * 5
numero1 *= 5 // 50


// atribui a divisão (/=)
// numero1 = numero1 / 5
numero1 /= 5 // 10



// atribui a divisão (%=)
// numero1 = numero1 % 5
numero1 %= 5 // 0


// atribui a exponenciação (**=)
// numero1 = numero1 ** 5
numero1 **= 5 // 0


// =================================================================
// 3 - RELACIONAIS
// uma forma de relacionarmos/comparar o valor da esquerda com o valor da direta
// o resultado dessa operação sempre será um booleano - TRUE ou FALSE
numero1 = 35
numero2 = 24.5
let numero3 = '35'

// maior que (>)
resultado = numero1 > numero2 // numero1 é maior que numero2? TRUE


// menor que (<)
resultado = numero1 < numero2 // numero1 é menor que numero2? FALSE


// igual que (==) -> valores
resultado = numero1 == numero2 // numero1 é igual ao numero2? FALSE



// estritamente igual que (===) -> valores e tipo
resultado = numero1 === numero3 // FALSE


// diferente de (!=) -> valores
resultado = numero1 != numero3 // FALSE


// estritamente diferente de (!==) -> valores e tipos
resultado = numero1 !== numero3 // TRUE


numero1 = 35
numero2 = 24.5

// maior OU igual que (>=)
resultado = numero1 >= numero2
// numero1 é maior que o numero2? TRUE
// numero1 é igual ao numero2? FALSE
// TRUE


// menor OU igual que (<=)
resultado = numero1 <= numero2
// numero1 é menor que o numero2? FALSE
// numero1 é igual ao numero2? FALSE
// FALSE



